const EmojiPicker: React.FC<EmojiPickerProps> = ({ onEmojiSelect, isDarkMode = false }) => {
  const [isOpen, setIsOpen] = useState(false);
  const [activeCategory, setActiveCategory] = useState<keyof typeof EMOJI_CATEGORIES>('Smileys');
  const pickerRef = useRef<HTMLDivElement>(null);
  const buttonRef = useRef<HTMLButtonElement>(null);

  useEffect(() => {
    const handleClickOutside = (e: MouseEvent) => {
      if (
        pickerRef.current && !pickerRef.current.contains(e.target as Node) &&
        buttonRef.current && !buttonRef.current.contains(e.target as Node)
      ) {
        setIsOpen(false);
      }
    };

    if (isOpen) {
      document.addEventListener('mousedown', handleClickOutside);
    }
    return () => document.removeEventListener('mousedown', handleClickOutside);
  }, [isOpen]);

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji);
    setIsOpen(false);
  };

  return (
    <div className="relative inline-block">
      <button
        ref={buttonRef}
        type="button"
        onClick={() => setIsOpen(!isOpen)}
        className={`p-2 rounded-xl transition-all duration-300 ${
          isOpen 
            ? 'bg-emerald-500 text-white shadow-lg shadow-emerald-500/20 scale-110' 
            : isDarkMode
              ? 'text-gray-400 hover:text-emerald-400 hover:bg-gray-700'
              : 'text-gray-500 hover:text-emerald-600 hover:bg-emerald-50'
        }`}
        title="Add emoji"
      >
        <Smile size={22} className={isOpen ? 'animate-bounce' : ''} />
      </button>

      {isOpen && (
        <div
          ref={pickerRef}
          className={`absolute bottom-full right-0 mb-4 z-[100] w-[320px] rounded-3xl shadow-2xl border transition-all duration-300 animate-in fade-in zoom-in slide-in-from-bottom-4 ${
            isDarkMode
              ? 'bg-gray-800/95 border-gray-700 backdrop-blur-xl'
              : 'bg-white/95 border-gray-200 backdrop-blur-xl'
          }`}
        >
          {/* Header */}
          <div className={`px-5 py-4 border-b flex items-center justify-between ${isDarkMode ? 'border-gray-700' : 'border-gray-200'}`}>
            <span className={`text-xs font-black uppercase tracking-widest ${isDarkMode ? 'text-emerald-400' : 'text-emerald-600'}`}>
              Select Emoji
            </span>
            <div className="flex gap-2">
              <div className="w-2 h-2 rounded-full bg-emerald-500 animate-pulse" />
              <div className="w-2 h-2 rounded-full bg-emerald-400" />
            </div>
          </div>

          {/* Category Tabs */}
          <div className={`flex gap-1 px-3 py-3 border-b overflow-x-auto no-scrollbar ${isDarkMode ? 'border-gray-700' : 'border-gray-200'}`}>
            {Object.keys(EMOJI_CATEGORIES).map((category) => (
              <button
                key={category}
                type="button"
                onClick={() => setActiveCategory(category as keyof typeof EMOJI_CATEGORIES)}
                className={`px-3 py-2 text-[10px] font-black uppercase tracking-tighter rounded-xl whitespace-nowrap transition-all ${
                  activeCategory === category
                    ? 'bg-emerald-500 text-white shadow-md'
                    : isDarkMode
                      ? 'text-gray-400 hover:bg-gray-700'
                      : 'text-gray-500 hover:bg-emerald-50 hover:text-emerald-600'
                }`}
              >
                {category}
              </button>
            ))}
          </div>

          {/* Emoji Grid */}
          <div className="p-4 overflow-y-auto no-scrollbar" style={{ maxHeight: '280px' }}>
            <div className="grid grid-cols-7 gap-2">
              {EMOJI_CATEGORIES[activeCategory].map((emoji, index) => (
                <button
                  key={index}
                  type="button"
                  onClick={() => handleEmojiClick(emoji)}
                  className={`text-2xl p-2 rounded-xl transition-all hover:scale-125 hover:bg-emerald-500/10 active:scale-95 ${
                    isDarkMode ? 'hover:text-white' : 'hover:text-emerald-600'
                  }`}
                  title={emoji}
                >
                  {emoji}
                </button>
              ))}
            </div>
          </div>

          {/* Footer Decorative Arrow */}
          <div className={`absolute bottom-0 right-5 w-4 h-4 translate-y-2 rotate-45 border-r border-b ${
            isDarkMode ? 'bg-gray-800 border-gray-700' : 'bg-white border-gray-200'
          }`} />
        </div>
      )}
    </div>
  );
};

export default EmojiPicker;
